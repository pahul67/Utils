package com.thomasww.tools.xml;

/**
 * @author Paul Thomas <paul@thomasww.com>
 *
 */
public class StringUtils {

	/**
	 * @param clear
	 * @return
	 * 
	 * @author Paul Thomas <paul@thomasww.com>
	 */
	public static String cypher(String clear) {
		String cyphered = "";
		cyphered = clear;
		return cyphered;
	}
	
	/**
	 * @param cyphered
	 * @return
	 * 
	 * @author Paul Thomas <paul@thomasww.com>
	 */
	public static String uncypher(String cyphered) {
		String clear = "";
		clear = cyphered;
		return clear;
	}
}
